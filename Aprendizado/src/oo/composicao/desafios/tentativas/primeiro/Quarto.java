package oo.composicao.desafios.tentativas.primeiro;

public class Quarto {
    String tipo;
    double precoPorNoite;

    public Quarto(String tipo, double precoPorNoite) {
        this.tipo = tipo;
        this.precoPorNoite = precoPorNoite;
    }

    public String getTipo() {
        return tipo;
    }
}
